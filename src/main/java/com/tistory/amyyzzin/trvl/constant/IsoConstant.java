    public static String convertCountryEngNm2Iso2(String countryEngNm) {
        int len = countryEngNm.length();
        countryEngNm = countryEngNm.charAt(len - 1) == ' ' ?
                countryEngNm.substring(0, len - 1) : countryEngNm;

        if (COUNTRY_ISOALP2_MAP.containsKey(countryEngNm)) {
            return COUNTRY_ISOALP2_MAP.get(countryEngNm);
        }

        return "";
    }
