    public static String convertCountryEngNm2Iso3(String countryEngNm) {
        int len = countryEngNm.length();
        countryEngNm = countryEngNm.charAt(len - 1) == ' ' ?
                countryEngNm.substring(0, len - 1) : countryEngNm;

        if (COUNTRY_ISOALP3_MAP.containsKey(countryEngNm)) {
            return COUNTRY_ISOALP3_MAP.get(countryEngNm);
        }

        return "";
    }
